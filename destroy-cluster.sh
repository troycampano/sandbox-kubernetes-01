#  BANNER
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
clear
echo -e "${BOLD}${RED}"
echo "  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•— в–Ҳв–Ҳв•—   в–Ҳв–Ҳв•—"
echo "  в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв•җв•җв•қв–Ҳв–Ҳв•”в•җв•җв•җв•җв•қв•ҡв•җв•җв–Ҳв–Ҳв•”в•җв•җв•қв–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•”в•җв•җв•җв–Ҳв–Ҳв•—в•ҡв–Ҳв–Ҳв•— в–Ҳв–Ҳв•”в•қ"
echo "  в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—   в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘ в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қ "
echo "  в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в–Ҳв–Ҳв•”в•җв•җв•қ  в•ҡв•җв•җв•җв•җв–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•”в•җв•җв–Ҳв–Ҳв•—в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘  в•ҡв–Ҳв–Ҳв•”в•қ  "
echo "  в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•—в–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘   в–Ҳв–Ҳв•‘  в–Ҳв–Ҳв•‘в•ҡв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв–Ҳв•”в•қ   в–Ҳв–Ҳв•‘   "
echo "  в•ҡв•җв•җв•җв•җв•җв•қ в•ҡв•җв•җв•җв•җв•җв•җв•қв•ҡв•җв•җв•җв•җв•җв•җв•қ   в•ҡв•җв•қ   в•ҡв•җв•қ  в•ҡв•җв•қ в•ҡв•җв•җв•җв•җв•җв•қ    в•ҡв•җв•қ   "
echo -e "${RESET}"
echo -e "${BOLD}  EKS Hello World вҖ” Cluster Destroy Script${RESET}"
echo -e "  Region: ${CYAN}$AWS_REGION${RESET} | Cluster: ${CYAN}$CLUSTER_NAME${RESET} | ECR Repo: ${CYAN}$ECR_REPO${RESET}"
divider
echo ""

# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
#  CONFIRMATION PROMPT
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
echo -e "${RED}${BOLD}  вҡ пёҸ  WARNING: This will permanently destroy:${RESET}"
echo -e "     вҖў EKS Cluster: ${BOLD}$CLUSTER_NAME${RESET}"
echo -e "     вҖў All running pods and Kubernetes resources"
echo -e "     вҖў The AWS Load Balancer"
echo -e "     вҖў ECR Repository: ${BOLD}$ECR_REPO${RESET} and all images"
echo -e "     вҖў Associated VPC, subnets, and IAM roles created by eksctl"
echo ""
echo -ne "${YELLOW}${BOLD}  Are you sure you want to continue? Type 'yes' to confirm: ${RESET}"
read CONFIRM

if [ "$CONFIRM" != "yes" ]; then
  echo ""
  echo -e "${GREEN}  Aborted. No changes were made.${RESET}"
  echo ""
  exit 0
fi

echo ""
info "Starting teardown of all resources..."
echo ""

# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
#  VERIFY AWS CREDENTIALS
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
divider
info "Verifying AWS credentials..."
divider
AWS_ACCOUNT_ID=$(aws sts get-caller-identity --query Account --output text 2>/dev/null) \
  || error "AWS credentials are not configured or have expired."
success "AWS credentials valid вҖ” Account ID: $AWS_ACCOUNT_ID"
echo ""

# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
#  STEP 1: DELETE KUBERNETES RESOURCES
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
divider
info "STEP 1/3 вҖ” Deleting Kubernetes resources (Deployment & Service)..."
divider
warn "Deleting the LoadBalancer Service will also decommission the AWS ELB..."

# Check if kubectl can reach the cluster
if kubectl cluster-info &>/dev/null; then
  # Delete service first so ELB is deprovisioned before cluster deletion
  if kubectl get service hello-world-service &>/dev/null 2>&1; then
    info "Deleting LoadBalancer service (deprovisioning AWS ELB)..."
    kubectl delete service hello-world-service
    warn "Waiting 30 seconds for ELB to fully deregister before cluster deletion..."
    for i in $(seq 30 -1 1); do
      echo -ne "  ${YELLOW}Waiting... ${i}s remaining${RESET}\r"
      sleep 1
    done
    echo ""
    success "LoadBalancer service deleted."
  else
    warn "Service 'hello-world-service' not found вҖ” may already be deleted."
  fi

  if kubectl get deployment hello-world &>/dev/null 2>&1; then
    info "Deleting Deployment..."
    kubectl delete deployment hello-world
    success "Deployment deleted."
  else
    warn "Deployment 'hello-world' not found вҖ” may already be deleted."
  fi
else
  warn "Could not connect to cluster вҖ” it may already be deleted. Skipping kubectl steps."
fi

echo ""

# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
#  STEP 2: DELETE EKS CLUSTER
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
divider
info "STEP 2/3 вҖ” Deleting EKS cluster '$CLUSTER_NAME'..."
info "This will delete the cluster, node group, VPC, subnets, and IAM roles."
info "This typically takes 10вҖ“15 minutes. Please be patient."
divider
warn "Handing off to eksctl вҖ” do not interrupt this process..."
echo ""

if eksctl get cluster --name $CLUSTER_NAME --region $AWS_REGION &>/dev/null 2>&1; then
  eksctl delete cluster \
    --name $CLUSTER_NAME \
    --region $AWS_REGION \
    --wait
  success "EKS cluster '$CLUSTER_NAME' fully deleted."
else
  warn "Cluster '$CLUSTER_NAME' not found in region '$AWS_REGION' вҖ” may already be deleted."
fi

echo ""

# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
#  STEP 3: DELETE ECR REPOSITORY
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
divider
info "STEP 3/3 вҖ” Deleting ECR repository '$ECR_REPO'..."
divider

if aws ecr describe-repositories --repository-names $ECR_REPO --region $AWS_REGION &>/dev/null 2>&1; then
  info "Deleting ECR repository and all images inside it..."
  aws ecr delete-repository \
    --repository-name $ECR_REPO \
    --region $AWS_REGION \
    --force
  success "ECR repository '$ECR_REPO' deleted."
else
  warn "ECR repository '$ECR_REPO' not found вҖ” may already be deleted."
fi

echo ""

# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
#  CLEAN UP LOCAL FILES (OPTIONAL)
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
divider
info "Cleaning up local project files..."
divider

if [ -d "eks-hello-world" ]; then
  echo -ne "${YELLOW}${BOLD}  Remove local 'eks-hello-world' project folder? (yes/no): ${RESET}"
  read REMOVE_LOCAL
  if [ "$REMOVE_LOCAL" = "yes" ]; then
    rm -rf eks-hello-world
    success "Local project folder removed."
  else
    info "Local project folder kept at: $(pwd)/eks-hello-world"
  fi
else
  warn "No local 'eks-hello-world' folder found вҖ” nothing to clean up."
fi

echo ""

# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
#  COMPLETE!
# в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
divider
echo -e "${GREEN}${BOLD}"
echo "  вң…  TEARDOWN COMPLETE!"
echo -e "${RESET}"
echo -e "  All AWS resources have been destroyed:"
echo -e "  ${GREEN}вң“${RESET} EKS Cluster deleted"
echo -e "  ${GREEN}вң“${RESET} Load Balancer deprovisioned"
echo -e "  ${GREEN}вң“${RESET} Node groups and VPC removed"
echo -e "  ${GREEN}вң“${RESET} ECR repository and images deleted"
echo ""
echo -e "  ${BOLD}No ongoing AWS charges remain from this setup.${RESET}"
echo ""
echo -e "  To rebuild the cluster from scratch, run:"
echo -e "  ${CYAN}  ./setup-cluster.sh${RESET}"
divider
echo ""
